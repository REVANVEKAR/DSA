package basics;

public class IncrementDecrement {
    public static void main(String[] args) {
       int i = 56;
        System.out.println(i--);
        System.out.println(--i);
        System.out.println(++i);
        System.out.println(i++);
       char ch = 'a';
        System.out.println(++ch);
    }
}
